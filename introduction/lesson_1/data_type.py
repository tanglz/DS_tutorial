# List
names = ['Bree', 'Matthew', 'Tom', 'Selena']
# # copy
new_list = names.copy()
# # len
print('The number of elements in the list:', len(names))
# # count
print('How many people with the name Bree?', names.count('Bree'))
# # index
print('The index of Tom is:', names.index('Tom'))
# # reverse
names.reverse()
print(names)
# # sort
names.sort()
print(names)
# # pop
print('Who is the last one?',names.pop())
# # del
print(new_list)
